"""
download_best_robots.py - Download the best robot for each task from the
HuggingFace EvolutionGym/robots dataset (90k+ robot structures).

For each target task, finds the robot with the highest reward and saves
its body and connections as a .npz file for use with run_eval.py.

Usage (from project root):
    pip install datasets
    python src/eval/download_best_robots.py

Output:
    src/eval/robots/<env_name>_best.npz   (one per task)
"""

import os
import sys
import numpy as np

sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), '..', '..')))

# Tasks we need optimal bodies for
TARGET_TASKS = [
    "UpStepper-v0",
    "ObstacleTraverser-v0",
    "Walker-v0",
    "BridgeWalker-v0",
]

def main():
    try:
        from datasets import load_dataset
    except ImportError:
        print("ERROR: 'datasets' package not found.")
        print("Install it with: pip install datasets")
        sys.exit(1)

    print("Loading EvoGym/robots dataset from HuggingFace...")
    print("(This may take a minute on first download)\n")
    ds = load_dataset("EvoGym/robots", split="train")

    out_dir = os.path.join(os.path.dirname(__file__), "robots")
    os.makedirs(out_dir, exist_ok=True)

    for task in TARGET_TASKS:
        # Filter dataset for this task
        task_robots = ds.filter(lambda x: x["env_name"] == task)
        n = len(task_robots)

        if n == 0:
            print(f"WARNING: No robots found for {task}, skipping.")
            continue

        # Find the robot with the highest reward
        rewards = task_robots["reward"]
        best_idx = int(np.argmax(rewards))
        best = task_robots[best_idx]

        best_body = np.array(best["body"])
        best_conn = np.array(best["connections"])
        best_reward = best["reward"]
        best_uid = best["uid"]
        best_algo = best.get("generated_by", "unknown")

        # Save as .npz (same format as EvoGym GA output)
        label = task.replace("-", "_")
        out_path = os.path.join(out_dir, f"{label}_best.npz")
        np.savez(out_path, best_body, best_conn)

        print(f"  {task}:")
        print(f"    Robots in dataset : {n}")
        print(f"    Best reward       : {best_reward:.3f}")
        print(f"    Best UID          : {best_uid}")
        print(f"    Generated by      : {best_algo}")
        print(f"    Body shape        : {best_body.shape}")
        print(f"    Saved to          : {out_path}")
        print()

    print("Done! Use these with run_eval.py via --robot-npz, e.g.:")
    print(f"  python src/eval/run_eval.py --robot-npz src/eval/robots/UpStepper_v0_best.npz ...")


if __name__ == "__main__":
    main()
